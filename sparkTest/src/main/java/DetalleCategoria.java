public class DetalleCategoria {

    String id;
    String name;
    String picture;
    String permalink;
    String totalItemsCategory;

    public DetalleCategoria() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getPermalink() {
        return permalink;
    }

    public void setPermalink(String permalink) {
        this.permalink = permalink;
    }

    public String getTotalItemsCategory() {
        return totalItemsCategory;
    }

    public void setTotalItemsCategory(String totalItemsCategory) {
        this.totalItemsCategory = totalItemsCategory;
    }
}
